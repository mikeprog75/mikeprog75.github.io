Les assurances
Sachez qu'il est possible de prendre une assurance auprès de son opérateur, des distributeurs, des banques. Cependant ces assurances coutent cher ! et ne couvrent pas tous les risques ! le montant de remplacement du téléphone est, suivant les contrats, limité ! bref, il faut scrupuleusement lire les contrats ! 

Il faut vérifier notamment que l'assurance couvre: les vols par effraction, les vols à la tire, à la sauvette, les dommages accidentels, les pertes accidentelles, le montant du plafond proposé pour remplacer le téléphone, la franchise par sinistre, le remplacement des accessoires volés, les communications passées après le vol sont-elles couvertes et jusqu'à quel montant.... 

Certaines assurances couvrent, en plus du téléphone, certains autres appareils (appareil photo, ordinateur ...) <br /><br />

Remarque: Au vu des prix pratiqués et du manque de clarté des assurances proposées, je ne conseille pas vraiment de prendre une assurance à ce jour... 


