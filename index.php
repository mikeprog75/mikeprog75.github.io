•	Antitheft de F secure Gratuit 
Ce logiciel permet de verrouiller son téléphone à distance, d'effacer les données qu'il contient, de le localiser, de connaitre le numéro de la personne qui a mis une nouvelle carte SIM, et le tout entièrement gratuitement ! Il est compatible Android, Symbian, Windows. 

En pratique avec Antitieft: 
•	Envoyez le message texte (SMS) : #lock#<votre code> à votre téléphone pour le verrouiller 
