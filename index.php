					<h1>Comment localiser un téléphone portable (smartphone) ?</h1>
					
					Article mis à jour le 17/10/2019 <br /><br />
					
Le vol de téléphone portable étant de plus en plus fréquent, les applications permettant de géolocaliser votre téléphone portable sont de plus en plus courantes et sont également très utiles en cas de perte. Les opérateurs téléphoniques ont la possibilité d'évaluer la distance entre chaque téléphone cellulaire et d'obtenir leurs coordonnées géographiques sur une carte grâce à la réception satellitaire. Ces données sont bien sûr sécurisées. Chaque « auteur » d'application, de programme ou de logiciel de géolocalisation rachète ces données pour les présenter sous forme de carte. Si votre téléphone vous a été dérobé, et si vous avez pré-enregistré vos mots de passe sur des sites (d'achat, Facebook, orange, free...) pensez à modifier vos mots de passe surtout ! 
 
Localiser le téléphone portable de quelqu'un
Techniquement, seul les opérateurs de téléphonie mobile peuvent localiser un appareil grâce à la technologie Cell-Id. 
Ils disposent de plusieurs dizaines de milliers de stations " de base ", réparties sur l'ensemble du territoire français. Chaque station de base permet d'arroser une zone géographique. Ainsi en connaissant la station à laquelle un abonné est connecté, il est possible d'avoir une approximation de la position grâce au rayon de couverture. Toutefois, plus la station est dans une zone ayant un faible nombre de stations de base, moins la précision sera importante. La précision va de quelques centaines de mètres en milieu urbain élevée, à plusieurs kilomètres en zone rurale. 

Généralement, un téléphone mobile capte le signal de plusieurs stations de base, si bien qu'il est possible "par triangulation" d'affiner la localisation d'un téléphone portable en croisant l'horodatage des données reçues par les différentes stations. 

Dans les faits, seule la gendarmerie peut demander à avoir ces informations, sur réquisition judiciaire. Notez qu'il est illégal de tenter de localiser une personnes sans son accord ! 

Dans les paragraphes ci-dessous, il est expliqué comment pouvoir localiser légalement son propre téléphone mobile. 
