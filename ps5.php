					<div id ="sony"><h2>La ps5: tous les détails sur la nouvelle console de Sony</h2></div>
					
					Article mis à jour le 19/10/2019 <br /><br />
					
	La ps5 s'appelera bel et bien ps5 ou playstation 5. Sony a officiellement donné des informations précises sur le nom de sa nouvelle console de jeux.
    La date de sortie de cette machine est prévue pour fin 2020 comme c'est le cas pour la Xbox Scarlett. Dans la suite de cet article, je vous donnerai toutes les informations détaillées sur cette
    nouvelle console à venir.

    <h3>Quelle date de lancement pour la PS5 ?</h3>
	
Sony vient de dévoiler officiellement la date de lancement de la PS5. Moins qu’une date, il s’agit d’une fenêtre : les vacances de fin 2020. Il pourrait s’agir des vacances de Thanksgiving (26 novembre 2020) qui apparaissent effectivement comme un timing parfait, juste avant le Black Friday et à temps pour être au pied du sapin.
Mais une toute nouvelle rumeur fait état de début décembre 2020 pour le lancement de cette nouvelle console. C’est le site TweakTown qui la relaie. Ce retard serait lié au fait que Sony aurait besoin de plus de temps pour obtenir les puces AMD dont elle a besoin pour produire suffisamment de consoles PS5. 
Un lancement début décembre serait une première venant de Sony, les dernières consoles (PS3, PS4 et PS4 pro) étaient toutes sorties au mois de novembre. Cette rumeur reste donc à perdre avec des pincettes.
Quelle que soit la date exacte cela devrait coïncider avec de lancement de la nouvelle Xbox de Microsoft. En revanche, le constructeur américain n’a pas encore dévoilé le nom commercial de sa prochaine console toujours provisoirement baptisée Scarlett.

Quoi qu’il en soit, on est assuré d’avoir une nouvelle guerre des consoles dans un an et ce sera pour notre plus grand plaisir. Sony et Microsoft devront redoubler d’idées pour capter les joueurs.
	
<h3>Quand la PS5 sera-t-elle présentée ?</h3>

Les plus gourmands pensent qu’elle pourrait être présentée avant la fin de l’année. C’est le cas d’un utilisateur de Reddit, RuthenicCookie. Pour lui, Sony se réserverait le PSX pour dévoiler sa nouvelle machine. Le PSX, c’est le Playstation Expérience, la grand-messe de la branche jeux vidéo de Sony. Et elle se déroule habituellement en décembre. Avec cette officialisation en octobre, le timing peut encore être respecté.
« La plupart des développeurs ont déjà des kits de développement spécifique à la PS5 (Spoiler : c’est un monstre), Sony se prépare pour la PS5 et l’une des raisons pour lesquelles ils n’iront pas à l’E3 est qu’ils ont vidé leurs chargeurs pour cette année et n’auront rien de nouveau à montrer l’année prochaine (ils veulent montrer de nouvelles choses au PSX) ».
RuthenicCookie

<h3>Ce qui reste de l’ordre de la rumeur</h3>

Selon les spécialistes, les dernières améliorations technologiques pourraient lui permettre dans tous les cas d’être jusqu’à 4 fois plus puissante que la PlayStation 4. La console pourrait ainsi remasteriser tous les jeux en 4K à la volée grâce à une combinaison de travail humain, et de détection automatisée « d’assets ». Ce brevet, déposé en 2016 et mis à jour en 2018, serait en train d’être intégré à la PS5.

La PS4 Pro est davantage vue du côté de Sony comme une console « rafraîchie » et considère déjà la suivante comme faisant partie du même cycle. La marque ne devrait pas renouveler cette erreur de son passé qui a donné un avantage technologique à Microsoft. On sait grâce à Digital Foundry que Mark Cerny fait bien partie de l’équipe de développement de la PS5. Il a déjà échangé avec de nombreux studios de développement et des éditeurs afin de comprendre leurs attentes et besoins pour proposer une console correspondant à ce qu’ils recherchent. Cerny est connu pour avoir travaillé sur la PS4 et choisi son architecture PC x86, qui a permis de créer une console sur laquelle les jeux sont plus facilement optimisés et à moindre prix. Il est souvent associé au succès de la PS4, qui avait succédé à une plus décevante PS3.

Si la rétrocompatibilité avec la PS4 est assurée, la prochaine console de Sony pourrait également prendre en charge d’autres modèles. Un leak suggère que la PlayStation 5 serait également compatible avec les anciennes consoles de Sony, à savoir la PS1, la PS2 et la PS3. Et toujours selon le même leak, la rétrocompatibilité serait assurée à 100%. de quoi gonfler considérablement le catalogue de la PS5 de plus de deux décennies de ludothèque. Reste à savoir si les jeux les plus anciens profiteront d’un petit traitement graphique ou s’ils s’afficheront tels quels.

Côté accessoires, la manette DualShock de la PS5 pourrait embarquer un écran tactile, semble indiquer un brevet déposé par Sony Interactive Entertainment. Une fonctionnalité qui pourrait permettre d’afficher et de faire défiler des informations en jeu sans encombrer l’interface principale par exemple. Ce que propose par exemple de faire Red Dead Redemption 2 avec son écran de smartphone via l’application Compagnon.

<h3>Combien coûtera la PS5 ?</h3>

Avancé par RuthenicCookie sur Reddit, la dernière rumeur en date fait état d’un tarif de 500 $. Ce serait un nouveau bond tarifaire pour Sony. Pour rappel, la PS4 avait été lancée à 399 $ (399 € en France) en 2013. Même schéma pour la PS4 Pro en 2016 qui s’affichait à un prix identique. Ici, la PS5 accuserait donc une hausse de 100 $ et donc de 100 € chez nous, logiquement. Bien évidemment, tout ceci est à prendre au conditionnel.

Mais le plus intéressant pour le moment reste l’échange entre le journaliste de Wired, Peter Rubin, et Mark Cerny, chef de projet de la PS4 : 
Pete Rubin : Il y a toujours eu une fourchette de prix de lancement. Est-ce que la prochaine console y correspondra ?

Mark Cerny : Je crois que nous serons capables de la commercialiser à un prix de vente conseillé qui séduira les joueurs compte tenu de ses fonctionnalités avancées.

Pete Rubin : Cela veut dire qu’elle pourrait coûter un peu plus, mais qu’elle vaudrait son coût ?

Mark Cerny : C’est tout ce que je peux en dire.
En somme, le joueur en aura pour son argent, selon Sony, mais la PS5 sera loin d’être bon marché. Sony oserait-il refaire le coup de la PS3 à 600 € ? 

<h3>Quel design aura la PS5 ?</h3>
En août dernier, le site Let’s Go Digital a intercepté un brevet déposé par Sony au sujet d’une console. Il s’agirait de la PS5 et l’on a même droit à quelques visuels. La machine est imposante, creusée d’un V (le 5 latin) en son centre et bardée d’ailettes et aérations en tout genre. On est loin d’une console sobre. Un design tellement atypique qu’il est encore soumis à caution. Sans officialisation de Sony on restera prudent sur ce point.

