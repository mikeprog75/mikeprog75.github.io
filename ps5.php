<h3>La PS5 aura du ray tracing (et de la 8K ?)</h3>

En clair, on aura droit à une forte puissance de calcul avec des composants de dernière génération, CPU et GPU en tête. À noter que la puce Navi supporte les définitions jusqu’en 8K, de quoi faire certifier la PS5 pour la prochaine génération de dalles TV (déjà existantes, mais très onéreuses). 
Autre bonne nouvelle, cette même partie graphique est compatible ray tracing. Ce mot barbare, à la mode chez les amateurs de cartes graphiques, définit une technologie qui permet de calculer l’ombre et la lumière en fonction de tous les éléments du décor. 
Et la PS5 accueillera du ray-tracing de manière matérielle et non logicielle. Sony se battra donc à armes égales avec Microsoft qui fera de même pour sa Xbox Scarlett. 
Reste à savoir si ce ray-tracing sera en temps réel ou accéléré. Nvidia nous rappelle que sur ce point, le résultat n’est pas le même. En temps réel, le ray-tracing est calculé pour l’image entière (360°). En accéléré, ce n’est que l’image vue par l’utilisateur à instant t. De facto, on perd toute la réflection du décor hors-champ.

<h3>Une PS5 avec 24 Go de RAM ?</h3>

Anthony Garreffa de TweakTown a partagé de nouvelles informations à ce sujet. Selon ses sources, la console embarquerait 24 Go de mémoire GDDR6 partagée dont 4 Go de DDR4 réservés pour l’OS.

<h3>Du SSD pour toutes les PS5 et jusqu’à 2 To</h3>

Il évoque également 2 To de stockage d’une « sorte de mémoire flash NAND », ce qui pourrait correspondre au SSD avancé par Mark Cerny. Un SSD améliorera considérablement les temps d’accès de la console. Chargement écourté, installation plus rapide, des petits plus agréables. Pour référence, Tom’s Hardware avait constaté un gain de temps de 18% entre un SSD et un disque dur sur une PS4 Slim.

<h3>Un nouveau casque VR pour la PS5 ?</h3>

C’est en tout cas ce que suggère RuthenicCookie. Sur Reddit, il indique que ce PlayStation VR 2 sera plus mobile, son boîtier étant alors intégré directement à la PS5. Il afficherait une meilleure définition. Le modèle actuel proposant du 1920 x 1080 au global, on peut espérer monter en Ultra HD sur le PSVR2.
Ce casque de réalité virtuelle s’accompagnerait d’une nouvelle PlayStation Camera et de nouveaux PS Move. Toujours selon la même source, Sony travaillerait également sur des gants compatibles.
La dernière information officielle émane du lead designer de la PS4 et maintenant de la PS5. Mark Cerny affirme que la réalité virtuelle demeure une priorité pour Sony. Aucune chance donc que l’idée soit abandonnée avec la PS5. S’il n’a pas souhaité répondre aux allégations d’un nouveau PSVR, il serait étonnant que Sony ne fasse pas évoluer son matériel.
