<h3>Dualshock 5 : une manette avec des vibrations dynamiques</h3>

Le prototype de manette de la PS5 a été montrée à un journaliste de Wired. Son nom n’a pas été indiqué, mais elle prendra vraisemblablement le flambeau de la lignée en s’appelant DualShock 5. Le journaliste la décrit comme étant prochaine de l’actuelle DualShock 4, laquelle avait été louée par les critiques pour son ergonomie. Sony en avait alors bien besoin après sa paresse avec la manette de la PS3.
Comme à chaque génération de console Sony, la manette de la PS5 apportera une petite nouveauté. On avait la détection de mouvement avec la PS3, le pad tactile avec la PS4. La PS5 introduira des gâchettes adaptatives. Il s’agira en réalité de gâchettes dynamiques dont la résistance sera modulée en fonction de la scène du jeu. Pour tendre un arc dans le prochain The Elder Scrolls, la pression sera souple au début de la course de la gâchette et se durcira au fur et à mesure, rendant la sensation de la tension de la corde entre les doigts.
Pour compléter cette description, Mark Cerny indique que la manette de la PS5 aura une plus grande batterie et une prise USB-C.<br />

