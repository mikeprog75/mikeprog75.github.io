<h3>De quelle puissance disposera la PS5 ?</h3>

Certes, on ne sait toujours pas de quoi sera capable la PS5, ou plutôt ce que son optimisation pourra garantir comme expérience à l’utilisateur. Néanmoins, on en sait un peu plus sur sa puissance brute grâce aux informations de Mark Cerny. Le responsable de la conception de la PS5 a étalé les caractéristiques principales de la machine : <br />
•	CPU : AMD Ryzen 3, 8 coeurs<br />
•	GPU : AMD Radeon Navi adapté (ray tracing/8K/audio 3D)<br />
•	Stockage SSD <br /><br />
PS5, Xbox 2 : les graphismes des consoles next-gen sont à tomber ! <br /><br />
